            历史并不是归类于人文科学或社会科学中，而是其间的桥梁，合并了两大领域的研究方法。一般来说，史学家通过研究各种书面文字但并不局限于此，努力并尝试解答和历史有关的问题。历史知识的原始资料分为三种：文字记载的、口头流传的、保留下来的历史遗迹，通常历史学家会综合三种方法进行研究，而文字记载经常被作为强调的重点，因为它普遍纪录了发展的时间。这种强调引申出了一个新领域，史前史，也可称为史前学，研究的是没有书面纪录的那一个时期。由于世界各地文字出现的时间各不相同，所以史前史和历史的主要区别是根据具体的论题而决定。学者们为了易于研究，根据过去人类的范围将其划分为不同的阶段。划分过去的方法繁多，包括按年代分类，按文化习俗分类，按不同主题分类。这三种分类经常会有重叠，比如“阿根廷的劳工运动的演变，1930-1945”。

            尽管历史研究倾向于一些专门的地点、时间和主题，历史学家也同时会关心其他普通的一些内容。而对于其他人来说，历史已经成为一个非常普通的词语，就是研究过去人类的所有事情，甚至于现在更兴起了一门所谓的广义历史。过去研究历史都是为了应用或者理论的目的，而现在还多了一条：那就是对人类过往的好奇。

            历史学
            主条目：史学
            以历史为认识对象所形成的一门学问，叫史学或历史学，也可以用“历史”一词代表。历史学的本质其实是把实际发生的事件转换成以意念和文字形式存在的历史的过程和方法。关于历史学的目的和方法的研究探讨，在西方属于历史哲学的范畴，历史哲学的出现和发展，意味着历史学从单纯的历史纪录发展成为对历史的解释和对历史规律的探求阶段。一开始，历史哲学仅仅关心如何改进历史研究的方法，但认为被研究和记录的历史就是真实的历史。在新康德主义和新黑格尔主义的影响下，人们对自身的认识过程有了重新的理解，哲学家开始重新定义历史学。意大利哲学家克罗齐提出“一切真历史都是当代史”的命题，认为往事只有在当代人生活中发挥作用才成为历史，否则是“死的历史”，即编年史。因此，同样的历史在不同的时期会被不断的改写。英国哲学家柯林武德又进一步认为“一切历史都是思想史”，即历史是历史学家思想的反映，不仅因时代而异，也因人而异。[2]而唯物主义的历史观认为历史事件是客观存在的，历史则是历史学家主观对客观的历史事件的认识。由于人主观的局限性，对客观的历史事件的认识是有限的，主观的认识不能完全符合客观的历史，因此只有不断改进逐渐逼近，这一过程同自然科学的过程一致。这种历史学称为“历史科学”。[6]

            历史和史前
            世界历史是世界各地人类过去经验的总和，而且这些经验主要是透过文字的方式保存下来。相对而言，史前是指一地区已有人类产生，但还没有出现文字的时代。借由研究当时的绘画、素描、雕刻或其他工件，可以在没有文字记录的情形下得到一些当时的资讯。自从二十世纪起，研究者开始重视史前的研究，以免历史研究隐性的排除一些特定的文明，例如撒哈拉以南非洲及前哥伦布时期的美洲。在西方的历史学家不成比例的专注在西方世界的研究[18]。1961年时，英国历史学家爱德华·霍列特·卡尔认为：

            当历史和史前的分界线交叉时，人们不再只活在现在，也对其过去和未来产生了兴趣。历史开始于对传统的传承，而传统代表着将过去的习惯及经验教训带到未来。过去的历史是因为对了要造福子孙后代而开始保存[19]。

