            史学史
            主条目：史学史
            中国是世界上书载历史的传承最完备的国家，其对历史的记录不仅时间长，而且内容精确详细。中国历史自传说中的黄帝以来已经有4千多年，而自西周共和时代（前841年）以来历史记录精确到年，自鲁隐公元年（前722年）以来则精确到月日。中国的历史记录也被称为史或史书，分为编年体，纪传体，纪事本末体等不同体裁。与西方文明中的历史学不占据主流地位相反，中国将史列为四种基本学科分类“经、史、子、集”之一（清纪晓岚等，《四库全书》）。[3]魁奈说：“历史学是中国人一直以其无与匹伦的热情予以研习的一门学问。没有什么国家如此审慎地撰写自己的编年史，也没有什么国家这样悉心地保存自己的历史典籍。”[20]

            原始社会中人类没有文字，只能通过诸如结绳记事和口传等方法作记录，一些历史的痕迹通过“传说”保存了下来，例如中国上古传说“黄帝战蚩尤”、“女娲补天”、“大禹治水”等。国家出现后，则开始有掌管祭祀的“巫”，他们同时兼任记录时事、起草公文和掌管文书等相关职能，可以说是最早的史官。之后随着国家职能的不断演进与发展，出现了职能独立的史官，专门记录历史事件，掌管典籍。在这个时期，中国出现了世界上最早的史书《尚书》，内容是历代政治文件汇编，并无特定的历史记录体裁。从西周共和元年（公元前841年）起，中国有了按年记载的编年史，从此有了连续不断的历史纪录，而且差不多每年都有史可查。这在世界各国范围内也是极其罕见的。春秋战国时期的史学家如孔子（编订《春秋》）和左丘明（著《左传》）等重视人类社会活动，从而使历史基本摆脱了神学和宗教的影响。[21]

            西汉时司马迁撰写了《史记》，创建了纪传体的历史记录体裁，《史记》的规模在当时世界范围内是空前的。之后东汉班固著《汉书》，延续发展了《史记》的体例，是中国第一部纪传体断代史。这两部历史著作，奠定了中国古典史学的基础，后来的历史学家沿用《史记》和《汉书》的体裁，将各个朝代的历史汇编成书，组成了“二十四史”。除断代史之外，唐宋期间中国还出现了通史，如唐末杜佑的《通典》，宋司马光的《资治通鉴》，其中《资治通鉴》是叙事长达一千三百六十二年的编年体通史，是中国史学史上的奇葩。[21]

            西方的历史学开始于公元前5世纪，古希腊作家希罗多德在《历史》（又名《希波战争史》）一书中记录了希腊与波斯之间的希波战争，历史从此自神话和文学中脱离出来成为独立的学科。希罗多德也因此被罗马哲学家西塞罗称为“史学之父”。但希罗多德的记录中真实事件与虚构事件混杂，并不是纯粹的历史。20多年后古希腊人修昔底德所著的《伯罗奔尼撒战争史》治学态度严谨，历史记载翔实，才是西方第一部“信史”。前2世纪，希腊历史学家波里比阿在《通史》（又名《罗马史》）中记录了前218年至前146年73年间罗马帝国周围地中海沿岸各国、各民族的历史，是第一部“世界”通史。[22]

            历史哲学
            主条目：历史哲学
            历史哲学是哲学的一个分支，主要考虑人类历史的最终意义。更进一步的，它考虑人类历史的可能的目的论的结局。换句话说，它追问人类历史的过程中是否存在着一个设计，目的，指导原则或是定局。

